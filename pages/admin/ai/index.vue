})

const api = useApi()
const message = useSafeMessage()

// жЁЎеһӢзҠ¶жҖҒдҝЎжҒ?const statusLoading = ref(false)
const serviceStatus = ref({
  type: 'default' as 'success' | 'warning' | 'error' | 'info' | 'default',
  text: 'жЈҖжҹҘдёӯ...',
  icon: 'fas fa-circle-notch fa-spin'
})

const modelInfo = ref({
  model: '',
  temperature: null as number | null,
  maxTokens: null as number | null
})

// жЈҖжҹ?AI жңҚеҠЎзҠ¶жҖ?const checkAiServiceStatus = async () => {
  statusLoading.value = true
  try {
    // и°ғз”ЁеҗҺз«ҜеҒҘеә·жЈҖжҹҘжҺҘеҸ?    const res = await api.get('/ai/health').catch(() => null)
    
    if (res && res.success) {
      serviceStatus.value = {
        type: 'success',
        text: 'иҝҗиЎҢдё?,
        icon: 'fas fa-check-circle'
      }
      
      // и®ҫзҪ®й»ҳи®ӨжЁЎеһӢдҝЎжҒҜпјҲеҸҜд»Ҙд»Һй…ҚзҪ®жҲ–е“Қеә”дёӯиҺ·еҸ–пј?      modelInfo.value = {
        model: 'DeepSeek',
        temperature: 0.7,
        maxTokens: 2000
      }
    } else {
      // еҰӮжһңеҗҺз«ҜжҺҘеҸЈдёҚеҸҜз”ЁпјҢе°қиҜ•зӣҙжҺҘи°ғз”Ё Python AI Service
      try {
        const healthRes = await $fetch('http://localhost:8001/api/ai/health', {
          method: 'GET',
          timeout: 3000
        }).catch(() => null)
        
        if (healthRes && healthRes.success) {
          serviceStatus.value = {
            type: 'success',
            text: 'иҝҗиЎҢдё?,
            icon: 'fas fa-check-circle'
          }
          modelInfo.value = {
            model: 'DeepSeek',
            temperature: 0.7,
            maxTokens: 2000
          }
        } else {
          throw new Error('жңҚеҠЎдёҚеҸҜз”?)
        }
      } catch {
        serviceStatus.value = {
          type: 'warning',
          text: 'жңӘиҝһжҺ?,
          icon: 'fas fa-exclamation-triangle'
        }
        modelInfo.value = {
          model: '',
          temperature: null,
          maxTokens: null
        }
      }
    }
  } catch (e: any) {
    serviceStatus.value = {
      type: 'error',
      text: 'иҝһжҺҘеӨұиҙҘ',
      icon: 'fas fa-times-circle'
    }
    modelInfo.value = {
      model: '',
      temperature: null,
      maxTokens: null
    }
    console.error('жЈҖжҹ?AI жңҚеҠЎзҠ¶жҖҒеӨұиҙ?', e)
  } finally {
    statusLoading.value = false
  }
}

// йЎөйқўеҠ иҪҪж—¶жЈҖжҹҘзҠ¶жҖ?onMounted(() => {
  checkAiServiceStatus()
})

